export default function MarketingAudit() {
  const [screen, setScreen] = useState('start'); // 'start' | 'testing' | 'results'
  const [currentQuestionIndex, setCurrentQuestionIndex] = useState(0);
  const [answers, setAnswers] = useState({});
  const [results, setResults] = useState(null);

  const currentQuestion = questionsDatabase[currentQuestionIndex];
  const totalQuestions = questionsDatabase.length;

  const handleStart = () => {
    setScreen('testing');
    setCurrentQuestionIndex(0);
    setAnswers({});
  };

  const handleAnswer = (answer) => {
    const newAnswers = {
      ...answers,
      [currentQuestion.id]: answer
    };
    setAnswers(newAnswers);

    if (currentQuestionIndex < totalQuestions - 1) {
      setCurrentQuestionIndex(currentQuestionIndex + 1);
    } else {
      // –ó–∞–≤–µ—Ä—à–µ–Ω–∏–µ —Ç–µ—Å—Ç–∞
      const calculatedResults = calculateResults(newAnswers, questionsDatabase);
      setResults(calculatedResults);
      setScreen('results');
    }
  };

  const handleBack = () => {
    if (currentQuestionIndex > 0) {
      setCurrentQuestionIndex(currentQuestionIndex - 1);
    }
  };

  const handleRestart = () => {
    setScreen('start');
    setCurrentQuestionIndex(0);
    setAnswers({});
    setResults(null);
  };

  return (
    <>
      <style>{globalStyles}</style>
      
      {screen === 'start' && (
        <StartScreen onStart={handleStart} />
      )}
      
      {screen === 'testing' && currentQuestion && (
        <QuestionScreen
          question={currentQuestion}
          currentIndex={currentQuestionIndex}
          totalQuestions={totalQuestions}
          onAnswer={handleAnswer}
          onBack={handleBack}
          canGoBack={currentQuestionIndex > 0}
        />
      )}
      
      {screen === 'results' && results && (
        <ResultsScreen
          results={results}
          onRestart={handleRestart}
        />
      )}
    </>
  );
}
